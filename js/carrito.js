const btnAbrirCarro = document.getElementsByClassName('btnCarrito');
const closeBtn = document.getElementsByClassName('btn-close');
const offcanvasCuerpo = document.getElementsByClassName('offcanvasBody')

btnAbrirCarro.addEventListener('click', () => {
    preventDefault();
    offcanvasCuerpo.show;
});

closeBtn.addEventListener('click', () => {
    preventDefault();
    offcanvasCuerpo;
});